    public void displayList() {
    	employeeList = em.findAll();
    	int i = 0;
		while(i < employeeList.size()) {
			Employee e = employeeList.get(i);
			model.addRow(new Object [] {
					e.getEmployeeCode(), e.getEmployeeName(), e.getBirthDay(), e.getAddress(), e.getNumberPhone(), e.getPosition()
			});
			i++;
		}
    }
