    private void tbkmMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbkmMouseClicked
        // TODO add your handling code here:
        int i=tbkm.getSelectedRow();
        if(i!=-1){
            KhuyenMaiDTO khuyenMaiDTO=new KhuyenMaiDTO();
            khuyenMaiDTO=KhuyenMaiBUS.danhSachKhuyenMai.get(i);
            txtma.setText(khuyenMaiDTO.getId());
            txtten.setText(khuyenMaiDTO.getTen());
            txtphantram.setText(Integer.toString(khuyenMaiDTO.getPhan_tram()));
            txtbd.setText(new SimpleDateFormat("dd/MM/yyyy").format(khuyenMaiDTO.getNgay_bat_dau()));
            txtkt.setText(new SimpleDateFormat("dd/MM/yyyy").format(khuyenMaiDTO.getNgay_ket_thuc()));
        }
    }//GEN-LAST:event_tbkmMouseClicked

    private void btthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btthemActionPerformed
        // TODO add your handling code here:
        KhuyenMaiDTO khuyenMaiDTO=new KhuyenMaiDTO();
        khuyenMaiDTO.setId(txtma.getText());
        khuyenMaiDTO.setTen(txtten.getText());
        khuyenMaiDTO.setPhan_tram(Integer.parseInt(txtphantram.getText()));
        try {
            khuyenMaiDTO.setNgay_bat_dau(new SimpleDateFormat("dd/MM/yyyy").parse(txtbd.getText()));
        } catch (ParseException ex) {
            Logger.getLogger(ChuongTrinhKhuyenMai.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            khuyenMaiDTO.setNgay_ket_thuc(new SimpleDateFormat("dd/MM/yyyy").parse(txtkt.getText()));
        } catch (ParseException ex) {
            Logger.getLogger(ChuongTrinhKhuyenMai.class.getName()).log(Level.SEVERE, null, ex);
        }
        KhuyenMaiBUS khuyenMaiBUS=new KhuyenMaiBUS();
        try {
            khuyenMaiBUS.them(khuyenMaiDTO);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ChuongTrinhKhuyenMai.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(ChuongTrinhKhuyenMai.class.getName()).log(Level.SEVERE, null, ex);
        }
        stt=0;
        model.setRowCount(0);
        try {
            hienThiCTKM();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ChuongTrinhKhuyenMai.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(ChuongTrinhKhuyenMai.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            Logger.getLogger(ChuongTrinhKhuyenMai.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btthemActionPerformed

