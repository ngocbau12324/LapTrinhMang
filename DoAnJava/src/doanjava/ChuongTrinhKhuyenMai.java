    private void hienThiCTKM() throws ClassNotFoundException, SQLException, ParseException{
        KhuyenMaiBUS khuyenMaiBUS=new KhuyenMaiBUS();
        khuyenMaiBUS.docDanhSachKhuyenMai();
        for(KhuyenMaiDTO kmdto: KhuyenMaiBUS.danhSachKhuyenMai){
            Vector row=new Vector();
            row.add(++stt);
            row.add(kmdto.getId());
            row.add(kmdto.getTen());
            row.add(new SimpleDateFormat("dd/MM/yyyy").format(kmdto.getNgay_bat_dau()));
            row.add(new SimpleDateFormat("dd/MM/yyyy").format(kmdto.getNgay_ket_thuc()));
            row.add(kmdto.getPhan_tram());
            model.addRow(row);
        }
    }
