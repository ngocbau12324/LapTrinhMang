    private void txt_ma1CaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_txt_ma1CaretUpdate
        // TODO add your handling code here:
        String idString=txt_ma1.getText();
        NhanVienBUS nhanVienBUS=new NhanVienBUS();
        NhanVienDTO nhanVienDTO=new NhanVienDTO();
        nhanVienDTO=nhanVienBUS.timkiem(idString);
        
        model.setRowCount(0);
        stt=1;
        Vector row=new Vector();
        row.add(stt);
        row.add(nhanVienDTO.getMaNhanVien());
        row.add(nhanVienDTO.getHo());
        row.add(nhanVienDTO.getTen());
        row.add(nhanVienDTO.getGioitinh());
        row.add(nhanVienDTO.getNgaysinh());
        row.add(nhanVienDTO.getDiachi());
        row.add(nhanVienDTO.getSdt());
        
        model.addRow(row);
        
        
        
        
    }//GEN-LAST:event_txt_ma1CaretUpdate

