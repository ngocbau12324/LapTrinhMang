    private void txt_ten1CaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_txt_ten1CaretUpdate
        // TODO add your handling code here:
        String tenString=txt_ten1.getText();
        NhanVienBUS nhanVienBUS= new NhanVienBUS();
        
        model.setRowCount(0);
        stt=0;
        try {
            for(NhanVienDTO nhanVienDTO : nhanVienBUS.timKiemTen(tenString)){
                Vector row=new Vector();
                    row.add(++stt);
                    row.add(nhanVienDTO.getMaNhanVien());
                    row.add(nhanVienDTO.getHo());
                    row.add(nhanVienDTO.getTen());
                    row.add(nhanVienDTO.getGioitinh());
                    row.add(nhanVienDTO.getNgaysinh());
                    row.add(nhanVienDTO.getDiachi());
                    row.add(nhanVienDTO.getSdt());
        
        model.addRow(row);
                
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(NhanVienGUI.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(NhanVienGUI.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            Logger.getLogger(NhanVienGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
        
    }//GEN-LAST:event_txt_ten1CaretUpdate

