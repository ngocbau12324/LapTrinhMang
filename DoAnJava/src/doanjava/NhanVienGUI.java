    void hienthinhanvien() throws ClassNotFoundException, SQLException, ParseException{
        NhanVienBUS nhanVienBUS=new NhanVienBUS();
        nhanVienBUS.docDanhSachNhanVien();
        for(NhanVienDTO nv : NhanVienBUS.danhSachNhanVien){
            Vector row=new Vector();
            row.add(++stt);
            row.add(nv.getMaNhanVien());
            row.add(nv.getHo());
            row.add(nv.getTen());
            row.add(nv.getGioitinh());
            row.add(new SimpleDateFormat("dd/MM/yyyy").format(nv.getNgaysinh()));
            row.add(nv.getDiachi());
            row.add(nv.getSdt());
            model.addRow(row);
        }
    }
