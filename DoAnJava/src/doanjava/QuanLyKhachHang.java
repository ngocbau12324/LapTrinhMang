    private void bt_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_xoaActionPerformed
        // TODO add your handling code here:
        int stt_hang = tbkhachhang.getSelectedRow();
        if (stt_hang == -1) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng chọn khách hàng để xóa");
        } else {
            int reply = JOptionPane.showConfirmDialog(null, "Bạn có chắc muốn xóa không?","", JOptionPane.YES_NO_OPTION);
            if (reply == JOptionPane.YES_OPTION) {
                model.removeRow(stt_hang);
                KhachHangBUS khachHangBUS=new KhachHangBUS();
                try {
                    khachHangBUS.xoa(txt_ma.getText());
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(QuanLyKhachHang.class.getName()).log(Level.SEVERE, null, ex);
                } catch (SQLException ex) {
                    Logger.getLogger(QuanLyKhachHang.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ParseException ex) {
                    Logger.getLogger(QuanLyKhachHang.class.getName()).log(Level.SEVERE, null, ex);
                }
                model.setRowCount(0);
                try {
                    hienThiKhachHang();
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(QuanLyKhachHang.class.getName()).log(Level.SEVERE, null, ex);
                } catch (SQLException ex) {
                    Logger.getLogger(QuanLyKhachHang.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ParseException ex) {
                    Logger.getLogger(QuanLyKhachHang.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        bt_lam_moi.doClick();
    }//GEN-LAST:event_bt_xoaActionPerformed

