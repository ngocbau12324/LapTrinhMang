    private void hienThiKhachHang() throws ClassNotFoundException, SQLException, ParseException {
        KhachHangBUS khachHangBUS = new KhachHangBUS();
        //if (KhachHangBUS.danhSachKhachHang == null) {
            khachHangBUS.docDanhSachKhachHang();
        
        for (KhachHangDTO khachHangDTO : KhachHangBUS.danhSachKhachHang) {
            Vector row = new Vector();
            row.add(++stt);
            row.add(khachHangDTO.getMaKhachHang());
            row.add(khachHangDTO.getHo());
            row.add(khachHangDTO.getTen());
            row.add(dateFormat.format(khachHangDTO.getNgaySinhDate()));
            row.add(khachHangDTO.getDiaChiString());
            row.add(khachHangDTO.getSoDienThoai());
            row.add(khachHangDTO.getEmail());
            model.addRow(row);
        }
    }
    
