    void hienThiTheLoai() throws ClassNotFoundException, SQLException, ParseException{
        TheLoaiBUS theLoaiBUS=new TheLoaiBUS();
        theLoaiBUS.docDanhSachTheLoai();
        for(TheLoaiDTO theLoaiDTO: TheLoaiBUS.danhSachTheLoai){
            Vector row=new Vector();
            row.add(++stt);
            row.add(theLoaiDTO.getMaTheLoaiString());
            row.add(theLoaiDTO.getTenTheLoaiString());
            model.addRow(row);
        }
    }
