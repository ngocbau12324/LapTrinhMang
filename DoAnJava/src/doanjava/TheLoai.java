    private void txttimCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_txttimCaretUpdate
        // TODO add your handling code here:
        TheLoaiBUS theLoaiBUS=new TheLoaiBUS();
        stt=0;
        model.setRowCount(0);
        try {
            for(TheLoaiDTO theLoaiDTO: theLoaiBUS.timkiemTen(txttim.getText())){
                Vector row=new Vector();
                row.add(++stt);
                row.add(theLoaiDTO.getMaTheLoaiString());
                row.add(theLoaiDTO.getTenTheLoaiString());
                model.addRow(row);
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(TheLoai.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(TheLoai.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_txttimCaretUpdate

