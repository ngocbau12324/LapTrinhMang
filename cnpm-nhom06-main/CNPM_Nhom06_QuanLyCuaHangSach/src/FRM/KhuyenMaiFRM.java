    private void btSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSuaActionPerformed
        // TODO add your handling code here:
        int i = jt.getSelectedRow();
    if(i>=0)
    {
        if(!isValidToAdd())
            return;
        int result = JOptionPane.showConfirmDialog(null, "Xác nhận sửa ?","Xác nhận",JOptionPane.YES_NO_OPTION);
        if(result == JOptionPane.YES_OPTION)
        {
            KhuyenMaiDTO km = getFromTextField();
            KhuyenMaiBUS bus = new KhuyenMaiBUS();
            int check = bus.sua(km,i);
            if(check == 1)
            {
                setModelValue(km,i);
                JOptionPane.showMessageDialog(null, "Sửa thành công");
            }
        }
    }    
    }//GEN-LAST:event_btSuaActionPerformed

    private void btXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btXoaActionPerformed
        // TODO add your handling code here:
        int i = jt.getSelectedRow();
        if(i>=0){
            int result = JOptionPane.showConfirmDialog(null, "Xác nhận xóa ?", "Xác nhận",JOptionPane.YES_NO_OPTION);
            if(result == JOptionPane.YES_OPTION){
                KhuyenMaiBUS bus = new KhuyenMaiBUS();
                int check = bus.xoa(i);  
                if(check == 1){
                    model.removeRow(i);
                    jt.setModel(model);
                    JOptionPane.showMessageDialog(null, "Xóa thành công");
                } 
            }
        }
    }//GEN-LAST:event_btXoaActionPerformed

