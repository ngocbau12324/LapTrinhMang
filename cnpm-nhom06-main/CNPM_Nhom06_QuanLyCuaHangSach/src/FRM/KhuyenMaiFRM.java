public boolean isValidToAdd()
{
        boolean ok = true;
        if(txtMa.getText().trim().equals("")) ok = false;
        if(txtNgaybd.getText().trim().equals("")) ok = false;
        if(txtNgaykt.getText().trim().equals("")) ok = false;
        if(ok == false)
        {
            JOptionPane.showMessageDialog(null, "Bạn phải điền đầy đủ thông tin !");
            return ok;
        }
        return ok;
}
 public KhuyenMaiDTO getFromTextField()
    {
            KhuyenMaiDTO km = new KhuyenMaiDTO();
            km.setMa(txtMa.getText().trim());
            km.setNgaybd(txtNgaybd.getText().trim());
            km.setNgaykt(txtNgaykt.getText().trim());
            km.setMota(txtMota.getText().trim());
            return km;
    }
 public Vector setVector(KhuyenMaiDTO km)
    {
            Vector head = new Vector();
            head.add(km.getMa());
            head.add(km.getNgaybd());
            head.add(km.getNgaykt());
            head.add(km.getMota());
            return head;
    }
 public void setModelValue(KhuyenMaiDTO km, int i)
    {
        model.setValueAt(km.getMa(),i,0);
        model.setValueAt(km.getNgaybd(),i,1);
        model.setValueAt(km.getNgaykt(),i,2);
        model.setValueAt(km.getMota(),i,3);
        jt.setModel(model);            
    }
 public void docKhuyenMai()
 {
       KhuyenMaiBUS bus = new KhuyenMaiBUS();
       try
       {
           bus.docKhuyenMai();
       }
       catch(Exception e)
       {
           JOptionPane.showMessageDialog(null, "Lỗi kết nối đến Database.");
           return;
       }
       Vector header = new Vector();
       header.add("Mã");
       header.add("Ngày bắt đầu");
       header.add("Ngày kết thúc");
       header.add("Mô tả");
       model = new DefaultTableModel(header,0)
       {
            public boolean isCellEditable(int row, int column)
                {
                  return false;//This causes all cells to be not editable
                }
       };
       jt.setAutoCreateRowSorter(true);	
       showOnTable(bus.ds);
    }
 public void showOnTable(ArrayList<KhuyenMaiDTO> ds)
    {
        model.setRowCount(0);
        for(KhuyenMaiDTO km:ds){
           Vector data = setVector(km);
           model.addRow(data);
       }
       jt.setModel(model);
    }
 public void setTextFieldValue(String ma, String ngaybd, String ngaykt, String mota)
    {
            txtMa.setText(ma);
            txtNgaybd.setText(ngaybd);
            txtNgaykt.setText(ngaykt);;
            txtMota.setText(mota);
    }
