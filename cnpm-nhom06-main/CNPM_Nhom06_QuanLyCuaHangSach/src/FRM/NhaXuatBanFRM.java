    public void doDuLieuLenTable() throws Exception{
        NhaXuatBanBUS bus = new NhaXuatBanBUS();
        bus.docDuLieu();
        table=(DefaultTableModel)jt.getModel();
        table.setRowCount(0);
        for(NhaXuatBanDTO ncc:bus.ds){
            Vector data = new Vector();
            data.add(ncc.getMa());
            data.add(ncc.getTen());
            data.add(ncc.getDiaChi());
            table.addRow(data);
        }
        jt.setModel(table);
    }
    public NhaXuatBanDTO getFromTextField(){
        NhaXuatBanDTO ncc = new NhaXuatBanDTO();
        ncc.setMa(txtMa.getText());
        ncc.setTen(txtTen.getText());
        ncc.setDiaChi(txtLoai.getText());
        return ncc;
    }
    public Vector setVector(){
        Vector head = new Vector();
        head.add(txtMa.getText());
        head.add(txtTen.getText());
        head.add(txtLoai.getText());
        return head;
    }
    public boolean isValidToAdd(){
        boolean ok = true;
        if(txtMa.getText().trim().length()<6) ok=false;
        if(txtTen.getText().trim().length()<2) ok= false;
        if(txtLoai.getText().trim().length()<2) ok= false;
        if(!ok){
            JOptionPane.showMessageDialog(null, "Hãy nhập đầy đủ thông tin !");
        }
        return ok;
    }
    public void setModelValue(NhaXuatBanDTO ncc, int i){
        table.setValueAt(ncc.getMa(),i,0);
        table.setValueAt(ncc.getTen(),i,1);
        table.setValueAt(ncc.getDiaChi(),i,2);
        jt.setModel(table);
    }
    public void setTextFieldValue(String ma,String ten,String loai){
        txtMa.setText(ma);
        txtTen.setText(ten);
        txtLoai.setText(loai);
    }
    public Vector setVector(NhaXuatBanDTO ncc){
        Vector vt = new Vector();
        vt.add(ncc.getMa());
        vt.add(ncc.getTen());
        vt.add(ncc.getDiaChi());
        return vt;
    }
    public void showOnTable(ArrayList<NhaXuatBanDTO> ds){
        table.setRowCount(0);
        for(NhaXuatBanDTO ncc:ds){
            Vector head = setVector(ncc);
            table.addRow(head);
        }
         jt.setModel(table);
    }
