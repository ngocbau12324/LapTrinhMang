    public boolean isValidToAdd(){
        boolean ok = true;
        if(txtMa.getText().trim().equals("")) ok = false;
        if(txtHo.getText().trim().equals("")) ok = false;
        if(txtTen.getText().trim().equals("")) ok = false;
        txtCa.setText(cbCaLam.getSelectedItem().toString() + " " + cbThu.getSelectedItem().toString());
        if(txtLuong.getText().trim().equals("")) ok = false;
        if(ok == false){
            JOptionPane.showMessageDialog(null, "Bạn phải điền đầy đủ thông tin !");
            return ok;
        }
        if(!isNumberic(txtLuong.getText())){
            ok = false;
            JOptionPane.showMessageDialog(null, "Lương không hợp lệ !");
        }
        return ok;
    }
    public void docNhanVien(){
       NhanVienBUS bus = new NhanVienBUS();
       try{
           bus.docNhanVien();
       }catch(Exception e){
           JOptionPane.showMessageDialog(null, "Lỗi kết nối đến Database.");
           return;
       }
       Vector header = new Vector();
       header.add("Mã");
       header.add("Họ");
       header.add("Tên");
       header.add("Ngày sinh");
       header.add("Giới tính");
       header.add("Ca làm");
       header.add("Lương");
       model = new DefaultTableModel(header,0){
            public boolean isCellEditable(int row, int column)
                {
                  return false;//This causes all cells to be not editable
                }
       };
       jt.setAutoCreateRowSorter(true);	
       showOnTable(bus.ds);
    }
    public void showOnTable(ArrayList<NhanVienDTO> ds){
        model.setRowCount(0);
        for(NhanVienDTO nv:ds){
           Vector data = setVector(nv);
           model.addRow(data);
       }
       jt.setModel(model);
    }
    public void taoNgaySinh(){
        Vector nam = new Vector();
        int year = Calendar.getInstance().get(Calendar.YEAR)-17;
        for(int i=year; i>=1960;i--){
            nam.add(i);
        }
        cbNam.setModel(new DefaultComboBoxModel(nam));
    }
    public void setDate(String ngaysinh){
        String[] ng = ngaysinh.split("/");
        cbNgay.setSelectedItem(Integer.parseInt(ng[0])+"");     
        cbThang.setSelectedIndex(Integer.parseInt(ng[1])-1);
        cbNam.setSelectedItem(Integer.parseInt(ng[2]));
    }
    public NhanVienDTO getFromTextField(){
            NhanVienDTO nv = new NhanVienDTO();
            nv.setMa(txtMa.getText().trim());
            nv.setHo(txtHo.getText().trim());
            nv.setTen(txtTen.getText().trim());
            nv.setNgaysinh(txtNgaySinh.getText().trim());
            nv.setGioitinh(rbNam.isSelected()?true:false);
            nv.setCa(txtCa.getText().trim());
            nv.setLuong(Integer.parseInt(txtLuong.getText()));
            return nv;
    }
    public Vector setVector(NhanVienDTO nv){
            Vector head = new Vector();
            head.add(nv.getMa());
            head.add(nv.getHo());
            head.add(nv.getTen());
            head.add(nv.getNgaysinh());
            head.add(nv.isGioitinh()?"Nam":"Nữ");
            head.add(nv.getCa());
            head.add(nv.getLuong());
            return head;
    }
    public void setModelValue(NhanVienDTO nv, int i){
        model.setValueAt(nv.getMa(),i,0);
        model.setValueAt(nv.getHo(),i,1);
        model.setValueAt(nv.getTen(),i,2);
        model.setValueAt(nv.getNgaysinh(),i,3);
        model.setValueAt(nv.isGioitinh()?"Nam":"Nữ",i,4);
        model.setValueAt(nv.getCa(),i,5);
        model.setValueAt(nv.getLuong(),i,6);
        jt.setModel(model);            
    }
    public NhanVienDTO getNhanVienFromModel(int i){
            NhanVienDTO nv = new NhanVienDTO();
            nv.setMa(jt.getValueAt(i,0)+"");
            nv.setHo(jt.getValueAt(i,1)+"");
            nv.setTen(jt.getValueAt(i,2)+"");
            nv.setNgaysinh(jt.getValueAt(i,3)+"");
            nv.setGioitinh((jt.getValueAt(i,4)+"").equals("Nam")?true:false);
            nv.setCa(jt.getValueAt(i,5)+"");
            nv.setLuong(Integer.parseInt(jt.getValueAt(i,6)+"")); 
            return nv;
    }
    public void setTextFieldValue(String ma, String ho, String ten, String ngaysinh,String gioitinh,String calam,String luong){
            txtMa.setText(ma);
            txtHo.setText(ho);
            txtTen.setText(ten);
            txtNgaySinh.setText(ngaysinh);
            setDate(txtNgaySinh.getText());
            if((gioitinh).toLowerCase().equals("nam")){
                rbNam.setSelected(true);
            }else rbNu.setSelected(true);
            txtCa.setText(calam);
            String[] ca = txtCa.getText().split(" ");
            cbCaLam.setSelectedItem(ca[0]);
            cbThu.setSelectedItem(ca[1] + " " + ca[2]);
            txtLuong.setText(luong);
    }
    public boolean isValidDateFormat(){
        int ngay = Integer.parseInt(cbNgay.getSelectedItem().toString());
        int thang = (cbThang.getSelectedIndex()+1);
        txtNgaySinh.setText((ngay>=10?ngay:"0"+ngay) + "/" + (thang>=10?thang:"0"+thang) + "/" + cbNam.getSelectedItem());
        SimpleDateFormat df = new SimpleDateFormat("dd/MM/yyyy");
        df.setLenient(false);
        try{
            Date da = df.parse(txtNgaySinh.getText());
        }catch(ParseException e){
            return false;
        }
        return true;
    }
    public boolean isNumberic(String str){
        return str.matches("^\\d+$");
    }
