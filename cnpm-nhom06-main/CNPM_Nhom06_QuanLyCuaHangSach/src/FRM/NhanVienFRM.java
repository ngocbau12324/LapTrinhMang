    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
            if(!isValidToAdd())  
                return;
            if(!isValidDateFormat()){
                JOptionPane.showMessageDialog(null, "Ngày sinh không hợp lệ!");
                return;
            }
            NhanVienDTO nv = getFromTextField();
            Vector head = setVector(nv);
            NhanVienBUS bus = new NhanVienBUS();
            int check = bus.them(nv);
            if(check == 1){
                model.addRow(head);
                jt.setModel(model);
                JOptionPane.showMessageDialog(null, "Thêm thành công");
            }else{
                JOptionPane.showMessageDialog(null, "Mã đã tồn tại. Thêm thất bại");
            } 
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        int i = jt.getSelectedRow();
        if(i>=0){
            if(!isValidToAdd())
                return;
            if(!isValidDateFormat()){
                JOptionPane.showMessageDialog(null, "Ngày sinh không hợp lệ!");
                return;
            }
            int result = JOptionPane.showConfirmDialog(null, "Xác nhận sửa ?","Xác nhận",JOptionPane.YES_NO_OPTION);
            if(result == JOptionPane.YES_OPTION){
                NhanVienDTO nv = getFromTextField();
                NhanVienBUS bus = new NhanVienBUS();
                int check = bus.sua(nv, i);
                if(check == 1){
                    setModelValue(nv,i);
                    JOptionPane.showMessageDialog(null, "Sửa thành công");
                }
            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

