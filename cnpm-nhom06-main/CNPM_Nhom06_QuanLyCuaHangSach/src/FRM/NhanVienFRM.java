    private void jMenuItem8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem8ActionPerformed
        // TODO add your handling code here:
        NhanVienBUS bus = new NhanVienBUS();
        //lay thong tin luong
        int tongluong = 0;
        int min = Integer.MAX_VALUE;
        int max = Integer.MIN_VALUE;
        int[] luong = new int[5];
        int[] buoi = new int[]{0,0,0};
        int[] thu = new int[]{0,0,0,0,0,0,0};
        for(NhanVienDTO nv:bus.ds){
            tongluong += nv.getLuong();
            if(min > nv.getLuong()) min = nv.getLuong();
            if(max < nv.getLuong()) max = nv.getLuong();
            
            String[] day = nv.getCa().split(" ");
            switch(day[0]){
                case "Sáng":
                    buoi[0]++;
                    break;
                case "Chiều":
                    buoi[1]++;
                    break;
                case "Tối":
                    buoi[2]++;
                    break;
            }
            switch(day[2]){
                case "2":
                    thu[0]++;
                    break;
                case "3":
                    thu[1]++;
                    break;
                case "4":
                    thu[2]++;
                    break;
                case "5":
                    thu[3]++;
                    break;
                case "6":
                    thu[4]++;
                    break;
                case "7":
                    thu[5]++;
                    break;
                case "Nhật":
                    thu[6]++;
                    break;
            }
        }
        luong[0]= bus.ds.size();
        luong[1] = tongluong;
        luong[2] = luong[1]/luong[0];
        luong[3] = min;
        luong[4] = max;
        JFileChooser choose = new JFileChooser();
        int result = choose.showSaveDialog(null);
        if(result == JFileChooser.APPROVE_OPTION){
            File file = choose.getSelectedFile();
            //PrintReportNhanVien pr = new PrintReportNhanVien(file.getAbsolutePath(), tongso, tongluong, min, max);
            try{
                WriteToPDF report = new WriteToPDF(file.getAbsolutePath(),luong,buoi,thu);
            }catch(Exception e){e.printStackTrace();}
            JOptionPane.showMessageDialog(null,new Object[]{"Ghi báo cáo thành công",file.getAbsolutePath()});
        }
        
    }//GEN-LAST:event_jMenuItem8ActionPerformed

