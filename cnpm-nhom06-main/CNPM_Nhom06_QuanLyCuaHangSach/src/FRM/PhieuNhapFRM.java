    public void taiDuLieuCacClassKhac(){
        PhieuNhapBUS bus = new PhieuNhapBUS();
        Vector nv = bus.load("nhanvien");
        Vector ncc = bus.load("nhacungcap");
        cbManv.setModel(new DefaultComboBoxModel(nv));
        cbMancc.setModel(new DefaultComboBoxModel(ncc));
    }
    public JComboBox createComboBox(String pre,int from, int to){
        Vector vt = new Vector();
        for(int i=from;i<=to;i++)
                vt.add(pre + i +"");
        JComboBox cb = new JComboBox(vt);
        return cb;
    }
    public void taoNgaySinh(){
        Vector nam = new Vector();
        int year = Calendar.getInstance().get(Calendar.YEAR);
        for(int i=year; i>=1960;i--){
            nam.add(i+"");
        }
        cbNam.setModel(new DefaultComboBoxModel(nam));
    }
    public void TaoTxtNgaySinh(){
        String ngay=cbNgay.getSelectedItem()+"";
        ngay=(ngay.length()<2?"0" + ngay:ngay);
        String thang=cbThang.getSelectedItem()+"";
        thang=thang.substring(6);
        thang=(thang.length()<2?"0"+thang:thang);
        String nam=cbNam.getSelectedItem()+"";
        txtNgay = ngay + "/" + thang + "/" + nam;
    }
    public Vector setVector(PhieuNhapDTO hd){
        Vector vt = new Vector();
        vt.add(hd.getMahd());
        vt.add(hd.getManv());
        vt.add(hd.getMancc());
        vt.add(hd.getNgaynhap());
        vt.add(hd.getTongchi());
        vt.add(hd.getGhichu());
        return vt;
    }
    public void setModelValue(PhieuNhapDTO hd, int i){
        model.setValueAt(hd.getMahd(),i,0);
        model.setValueAt(hd.getManv(),i,1);
        model.setValueAt(hd.getMancc(),i,2);
        model.setValueAt(hd.getNgaynhap(),i,3);
        model.setValueAt(hd.getTongchi(),i,4);
        model.setValueAt(hd.getGhichu(),i,5);
        jt.setModel(model);
    }
    public void showOnTable(ArrayList<PhieuNhapDTO> ds){
        model=(DefaultTableModel)jt.getModel();
        model.setRowCount(0);
        for(PhieuNhapDTO hd:ds){
            Vector head = setVector(hd);
            model.addRow(head);
        }
        jt.setModel(model);
    }
    public void docDuLieu(){
        txtTong.setEditable(false);
        PhieuNhapBUS bus = new PhieuNhapBUS();
        try{
            bus.docDuLieu();
            showOnTable(bus.ds);
            taiDuLieuCacClassKhac();
        }catch(Exception e){}
        
    }
    public PhieuNhapDTO getFromTextField(){
        PhieuNhapDTO hd = new PhieuNhapDTO();
        hd.setMahd(txtMahd.getText());
        hd.setManv(cbManv.getSelectedItem()+"");
        hd.setMancc(cbMancc.getSelectedItem()+"");
        hd.setNgaynhap(txtNgay);
        if(!txtTong.getText().equals("")){
            hd.setTongchi(Integer.parseInt(txtTong.getText()));
        }else{
            hd.setTongchi(0);
        }
        hd.setGhichu(txtGhichu.getText());
        return hd;
    }
    public void setTextFieldValue(int i){
        txtMahd.setText(jt.getValueAt(i,0)+"");
        cbManv.setSelectedItem(jt.getValueAt(i,1));
        cbMancc.setSelectedItem(jt.getValueAt(i,2));
        String date = jt.getValueAt(i,3)+"";
        String[] comboDate = date.split("/");
        //combo 0 1 2
        cbNgay.setSelectedIndex(Integer.parseInt(comboDate[0])-1);
        cbThang.setSelectedIndex(Integer.parseInt(comboDate[1])-1);
        cbNam.setSelectedItem(comboDate[2]);
        txtTong.setText(jt.getValueAt(i,4)+"");
        txtGhichu.setText(jt.getValueAt(i,5)+"");
    }
    public boolean isValidToAdd(){
        
        if(txtMahd.getText().trim().length() < 5){
            JOptionPane.showMessageDialog(null, "Mã không hợp lệ");
            return false;
        }
        return true;
    }
