    public void HienThiDanhSachKhachHang(ArrayList<khachhangDTO> ds){
        this.model.setRowCount(0);
        Vector data;
        for(khachhangDTO kh:ds){
            data = new Vector();
            data.add(kh.getId());
            data.add(kh.getName());
            data.add(kh.getGioitinh());
            data.add(kh.getDiachi());
            data.add(kh.getSdt());
            this.model.addRow(data);
        }
        this.tbl_khachhang.setModel(model);
    }
