    public void showListKhachhang() throws SQLException
    {
        khachhangBUS nv = new khachhangBUS();
        try 
        {
            listKH = nv.getAllKhachHang();
            
            if(listKH!=null)
            {
                model = (DefaultTableModel) tbl_khachhang.getModel();
                listKH.forEach((lx) -> 
                {
                    model.addRow(new Object[]{lx.getId(),lx.getName(),lx.getGioitinh(),lx.getDiachi(),lx.getSdt(),false});
                });
            tbl_khachhang.setModel(model);
            }
            else
            {
                System.out.println("Không thêm được dữ liệu vào List || khachhangGUI");
            }
        } catch (SQLException ex) {
            Logger.getLogger(khachhangFRM.class.getName()).log(Level.SEVERE, null, ex);
        }
        rowSorter = new TableRowSorter<>(tbl_khachhang.getModel());
        TableSortFilter();
    }
    private void TableSortFilter() {
        tbl_khachhang.setRowSorter(rowSorter);
        txt_timkiem.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) {
                String text = txt_timkiem.getText();
                if (text.trim().length() == 0) {
                    rowSorter.setRowFilter(null);
                }
                else {
                    rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" +text));
                }
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                String text = txt_timkiem.getText();

                if (text.trim().length() == 0) {
                    rowSorter.setRowFilter(null);
                } else {
                    rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + text));
                }
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
                throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
            }
        });
        
    }
    public int getmaxid(ArrayList<khachhangDTO> l)
    {
        if (l.size()!=0)
            return l.get(l.size()-1).getId();
        return -1;
    }
