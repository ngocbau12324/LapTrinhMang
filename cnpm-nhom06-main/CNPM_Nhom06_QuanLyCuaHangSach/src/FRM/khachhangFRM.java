    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        // TODO add your handling code here:
            
            if(JOptionPane.showConfirmDialog(this, "Bạn muốn thêm?", "??", JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION)
            {
                try 
                    {
                        String diachi = txt_diachi.getText();
                        String gioitinh = cmb_gioitinh.getSelectedItem().toString();
                        String sdt = txt_sdt.getText();
                        int id = getmaxid(listKH)+1;
                        String name = txt_tenkhachhang.getText();
                        khachhangDTO nv = new khachhangDTO(diachi,gioitinh,sdt,id,name);
                        khachhangBUS bus = new khachhangBUS();
                        try {
                            bus.Insert(nv);
                        } catch (SQLException ex) {
                            Logger.getLogger(khachhangFRM.class.getName()).log(Level.SEVERE, null, ex);
                        }
                        listKH.add(nv);
                        DefaultTableModel model = (DefaultTableModel) tbl_khachhang.getModel();
                        model.addRow(new Object[]{nv.getId(),nv.getName(),nv.getGioitinh(),nv.getDiachi(),nv.getSdt()});
                    } 
                catch (NumberFormatException e)
                    {
                        JOptionPane.showMessageDialog(this,"Số điện thoại không được bỏ trống hoặc có chữ cái");
                    }
            }
    }//GEN-LAST:event_btn_themActionPerformed

    public void editModel(khachhangDTO nv, int id)
    {
        int row = returnposition(id);
        model.setValueAt(nv.getName(),row,1);
        model.setValueAt(nv.getGioitinh(),row,2);
        model.setValueAt(nv.getDiachi(),row,3);
        model.setValueAt(nv.getSdt(),row,4);
    }
    private int returnposition(int id) {
        for(int i=0; i<listKH.size(); i++) {
            if (listKH.get(i).getId()==id)
                return i;
        }
        return -1;
    }
    private void btn_luuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_luuActionPerformed
        // TODO add your handling code here:
        if(JOptionPane.showConfirmDialog(this,"Bạn muốn sửa?", "???",JOptionPane.YES_NO_OPTION)== JOptionPane.YES_OPTION)
        {
            try
            {
                if(tbl_khachhang.getSelectedRow()<0)
                {
                    JOptionPane.showMessageDialog(this, "Chưa Có khách hàng được chọn");
                }
                else
                {
                    String diachi = txt_diachi.getText();
                    String gioitinh = cmb_gioitinh.getSelectedItem().toString();
                    String sdt = txt_sdt.getText();
                    String name = txt_tenkhachhang.getText();
                    int id = Integer.parseInt(tbl_khachhang.getValueAt(tbl_khachhang.getSelectedRow(), 0).toString());
                    khachhangDTO nv = new khachhangDTO(diachi, gioitinh, sdt, id, name);
                    khachhangBUS bus = new khachhangBUS();
                    bus.Edit(nv);
                    listKH.set(returnposition(id), nv);
                    editModel(nv, id);
                }
            }
            catch(NumberFormatException e)
            {
                JOptionPane.showMessageDialog(this, "Số Điện Thoại Không Được Bỏ Trống Hoặc Có Chữ Cái");
            }
        }
        
    }//GEN-LAST:event_btn_luuActionPerformed
    
